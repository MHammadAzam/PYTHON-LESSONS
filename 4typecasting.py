# check type of variable

x = 5
print(type(x)) # <class 'int'>

y = 3.14
print(type(y)) # <class 'float'>

z = "Hello"
print(type(z)) # <class 'str'>

# typecasting
a = 10
b = 3.14
c = str(a) # typecasting int to str
print(c) # '10'
print(type(c)) # <class 'str'>

d = int(b) # typecasting float to int
print(d) # 3
print(type(d)) # <class 'int'>